package io.github.renatolsjf.chassis.validation;

public class ValidatorExecutionException extends RuntimeException {

    public ValidatorExecutionException() { super(); }
    public ValidatorExecutionException(String message) { super(message); }
    public ValidatorExecutionException(Throwable cause) { super(cause); }
    public ValidatorExecutionException(String message, Throwable cause) { super(message, cause); }

}
